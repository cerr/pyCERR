def generateBaseDataset(file_name) -> FileDataset:
    file_meta = getFileMeta()
    ds = FileDataset(file_name, {}, file_meta=file_meta, preamble=b"\0" * 128)
    addRequiredElementsToDs(ds)
    addSequenceListsToDs(ds)
    return ds


