def getRefImgSeq(series_data):
    refd_img_seq = Sequence()
    for img in series_data:
        ref_img_ds = Dataset()
        ref_img_ds.ReferencedSOPClassUID = getattr(img, 'SOPClassUID', generate_uid())
        ref_img_ds.ReferencedSOPInstanceUID = getattr(img, 'SOPInstanceUID', generate_uid())
        refd_img_seq.append(ref_img_ds)
    return refd_img_seq


def addRefSeriesSequence(ds: FileDataset, series_data):
    ref_series_seq = Sequence()
    ref_series_ds = Dataset()
    ref_series_ds.ReferencedInstanceSequence = getRefImgSeq(series_data)
    ref_series_ds.SeriesInstanceUID = getattr(series_data[0], 'SeriesInstanceUID', generate_uid())
